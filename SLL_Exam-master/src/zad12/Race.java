package zad12;

import java.util.Scanner;

class SLLNode<E> {
    protected E element;
    protected SLLNode<E> succ;

    public SLLNode(E elem, SLLNode<E> succ) {
        this.element = elem;
        this.succ = succ;
    }
}

class SLL<E> {
    private SLLNode<E> first;

    public SLL() {
        // Construct an empty SLL
        this.first = null;
    }

    public void deleteList() {
        first = null;
    }

    public int size() {
        int listSize = 0;
        SLLNode<E> tmp = first;
        while(tmp != null) {
            listSize++;
            tmp = tmp.succ;
        }
        return listSize;
    }

    @Override
    public String toString() {
        String ret = new String();
        if (first != null) {
            SLLNode<E> tmp = first;
            ret += tmp.element;
            while (tmp.succ != null) {
                tmp = tmp.succ;
                ret += "->" + tmp.element;
            }
        } else
            ret = "Prazna lista!!!";
        return ret;
    }

    public void insertFirst(E o) {
        SLLNode<E> ins = new SLLNode<E>(o, null);
        ins.succ = first;
        //SLLNode<E> ins = new SLLNode<E>(o, first);
        first = ins;
    }

    public void insertAfter(E o, SLLNode<E> node) {
        if (node != null) {
            SLLNode<E> ins = new SLLNode<E>(o, node.succ);
            node.succ = ins;
        } else {
            System.out.println("Dadenot jazol e null");
        }
    }
    public void insertBefore(E o, SLLNode<E> before) {

        if (first != null) {
            SLLNode<E> tmp = first;
            if(first==before){
                this.insertFirst(o);
                return;
            }
            //ako first!=before
            while (tmp.succ != before && tmp.succ!=null)
                tmp = tmp.succ;
            if (tmp.succ == before) {
                tmp.succ = new SLLNode<E>(o, before);;
            } else {
                System.out.println("Elementot ne postoi vo listata");
            }
        } else {
            System.out.println("Listata e prazna");
        }
    }

    public void insertLast(E o) {
        if (first != null) {
            SLLNode<E> tmp = first;
            while (tmp.succ != null)
                tmp = tmp.succ;
            tmp.succ = new SLLNode<E>(o, null);
        } else {
            insertFirst(o);
        }
    }

    public E deleteFirst() {
        if (first != null) {
            SLLNode<E> tmp = first;
            first = first.succ;
            return tmp.element;
        } else {
            System.out.println("Listata e prazna");
            return null;
        }
    }

    public E delete(SLLNode<E> node) {
        if (first != null) {
            SLLNode<E> tmp = first;
            if(first == node) {
                return this.deleteFirst();
            }
            while (tmp.succ != node && tmp.succ.succ != null)
                tmp = tmp.succ;
            if (tmp.succ == node) {
                tmp.succ = tmp.succ.succ;
                return node.element;
            } else {
                System.out.println("Elementot ne postoi vo listata");
                return null;
            }
        } else {
            System.out.println("Listata e prazna");
            return null;
        }

    }

    public SLLNode<E> getFirst() {
        return first;
    }

    public SLLNode<E> find(E o) {
        if (first != null) {
            SLLNode<E> tmp = first;
            while (!tmp.element.equals(o) && tmp.succ != null)
                tmp = tmp.succ;
            if (tmp.element.equals(o)) {
                return tmp;
            } else {
                System.out.println("Elementot ne postoi vo listata");
            }
        } else {
            System.out.println("Listata e prazna");
        }
        return null;
    }

    public void merge (SLL<E> in){
        if (first != null) {
            SLLNode<E> tmp = first;
            while(tmp.succ != null)
                tmp = tmp.succ;
            tmp.succ = in.getFirst();
        }
        else{
            first = in.getFirst();
        }
    }

    public void mirror() {
        if (first != null) {
            //m=nextsucc, p=tmp,q=next
            SLLNode<E> tmp = first;
            SLLNode<E> newsucc = null;
            SLLNode<E> next;

            while(tmp != null){
                next = tmp.succ;
                tmp.succ = newsucc;
                newsucc = tmp;
                tmp = next;
            }
            first = newsucc;
        }
    }
}


class Athlete {
    private int id;
    private float time;

    public Athlete(int id, float time) {
        this.id = id;
        this.time = time;
    }

    public int getId() {
        return id;
    }

    public float getTime() {
        return time;
    }

    @Override
    public String toString() {
        return String.valueOf(id);
    }
}

public class Race {
    public static void competition(SLL<Athlete> prevWinners, SLL<Athlete> currYearRunners) {
        SLLNode<Athlete> prevWinnerIterator = prevWinners.getFirst();
        SLLNode<Athlete> currYearRunnersIterator = currYearRunners.getFirst();

        double worstTime = 0;
        while (prevWinnerIterator != null) {
            if (prevWinnerIterator.element.getTime() > worstTime) {
                worstTime = prevWinnerIterator.element.getTime();
            }
            prevWinnerIterator = prevWinnerIterator.succ;
        }

        while (currYearRunnersIterator != null) {
            if (currYearRunnersIterator.element.getTime() > worstTime) {
                currYearRunners.delete(currYearRunnersIterator);
            }

            currYearRunnersIterator = currYearRunnersIterator.succ;
        }
    }

    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);
        int num1 = Integer.parseInt(scanner.nextLine());
        int num2 = Integer.parseInt(scanner.nextLine());

        SLL<Athlete> olimpijada = new SLL<Athlete>();
        SLL<Athlete> tekovna = new SLL<Athlete>();

        for (int i = 0; i < num1; i++) {
            String line = scanner.nextLine(); // 1 1.85
            String[] parts = line.split("\\s+"); // {1, 1.85}
            olimpijada.insertLast(new Athlete(Integer.parseInt(parts[0]), Float.parseFloat(parts[1])));
        }

        for (int i = 0; i < num2; i++) {
            String line = scanner.nextLine();
            String[] parts = line.split("\\s+");
            tekovna.insertLast(new Athlete(Integer.parseInt(parts[0]), Float.parseFloat(parts[1])));
        }

        competition(olimpijada, tekovna);
        System.out.println(tekovna);
    }
}
